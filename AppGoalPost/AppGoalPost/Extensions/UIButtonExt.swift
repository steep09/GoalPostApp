import UIKit

extension UIButton {
    func setSeletedColor() {
        self.backgroundColor = #colorLiteral(red: 0.317841053, green: 0.8374810219, blue: 0.4631872773, alpha: 1)
    }
    func setDeselectColor() {
        self.backgroundColor = #colorLiteral(red: 0.7464011312, green: 0.8857318759, blue: 0.7402122021, alpha: 0.9914997799)
    }
}
